#include "libft.h"

void	ft_rev(char *s, int n)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = ft_strlen(s) - 1;
	if (n < 0)
		i++;
	while (i < j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		j--;
		i++;
	}
}
