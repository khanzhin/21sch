#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*s;

	s = b;
	i = 0;
	while (len != 0)
	{
		s[i] = (unsigned char)c;
		i++;
		len--;
	}
	return (s);
}
