#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*d1;

	d1 = dst;
	while (len != 0)
	{
		if (*src != '\0')
		{
			*dst++ = *src++;
		}
		else
			*dst++ = '\0';
		len--;
	}
	return (d1);
}
