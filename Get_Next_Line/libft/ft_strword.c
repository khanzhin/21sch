int	ft_strword(char const *str, char c)
{
	int wd;

	wd = 0;
	while (*str != '\0')
	{
		if (*str != c && (*(str + 1) == '\0' || *(str + 1) == c))
			wd++;
		str++;
	}
	return (wd);
}
