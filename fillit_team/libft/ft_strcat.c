#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int l;

	i = 0;
	l = 0;
	if (s1 == NULL && s2 == NULL)
		return (NULL);
	while (s1[i] != '\0')
		i++;
	while (s2[l] != '\0')
	{
		s1[i] = s2[l];
		l++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
