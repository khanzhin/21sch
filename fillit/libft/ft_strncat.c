#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t l;

	i = 0;
	l = 0;
	if (s1 == NULL && s2 == NULL)
		return (NULL);
	while (s1[i] != '\0')
		i++;
	while (s2[l] != '\0' && n != 0)
	{
		s1[i] = s2[l];
		l++;
		i++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
