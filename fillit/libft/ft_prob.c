int	ft_prob(const char *str, int k)
{
	while (str[k] == '\n' || str[k] == '\t' ||
			str[k] == '\b' ||
			str[k] == '\v' || str[k] == ' ' ||
			str[k] == '\r' ||
			str[k] == '\f' || str[k] == '\a')
	{
		k++;
	}
	return (k);
}
