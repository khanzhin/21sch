int	ft_digitnumber(int n)
{
	int k;

	k = 0;
	if (n < 0)
	{
		n = -n;
		k++;
	}
	if ((n / 10) == 0)
		return (1);
	else
	{
		while (n > 0)
		{
			n = n / 10;
			k++;
		}
		return (k);
	}
}
